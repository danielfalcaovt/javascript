let a = "a"
let num = 5
switch (a){
    case "+":
        console.log(num+num)
    break
    case "-":
        console.log(num-num)
    break
    case "*":
        console.log(num*num)
    break
    case "/":
        console.log(num/num)
    break
   
    default:
        console.log("Sem operador")
}